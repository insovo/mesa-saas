// 图标映射生成器 —— 解决 lucide-react 被全量打包的问题。
//
// 背景:Primitives 的 <I name="kebab" /> 早期用 `import * as Lucide` + 动态查找,
// 导致 tree-shaking 失效、整个 lucide 库(gzip ~150KB)进首屏。本脚本扫描源码里
// 所有实际用到的图标名,生成一份「显式命名导入」的映射文件 src/lib/iconMap.js,
// 让打包器只保留用到的图标。
//
// 用法(新增图标后重跑):  node scripts/gen-icon-map.mjs
// I 组件在映射缺失时回退 HelpCircle 并在 dev 下 console.warn,所以即便漏抓也不会崩。
//
// 图标名来源(覆盖所有 <I> 渲染路径):
//   1. <I name="..."> / <I name={cond ? "a" : "b"}>  —— 标签内字面量
//   2. icon: "..."   —— constants tone / Sidebar 菜单 / JdDescModal 等数据对象
//   3. name: "...", bg|title:  —— Upload 分享按钮数组(<I name={c.name}>)

import { readdirSync, readFileSync, statSync, writeFileSync, existsSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const here = dirname(fileURLToPath(import.meta.url));
const srcDir = join(here, "..", "src");
const outFile = join(srcDir, "lib", "iconMap.js");
// lucide-react 1.x 的 package.json 无 exports 字段、主入口是 barrel,rolldown 无法
// 从中 tree-shake → 直接深度导入单图标文件(文件名即 kebab name)绕过 barrel。
const iconDir = join(here, "..", "node_modules", "lucide-react", "dist", "esm", "icons");
const importPath = (name) => `lucide-react/dist/esm/icons/${name}.mjs`;

function pascal(name) {
  return name.split("-").map((s) => s.charAt(0).toUpperCase() + s.slice(1)).join("");
}

const names = new Set();
function walk(dir) {
  for (const f of readdirSync(dir)) {
    const p = join(dir, f);
    if (statSync(p).isDirectory()) { walk(p); continue; }
    if (!/\.(jsx?|js)$/.test(f)) continue;
    if (p === outFile) continue;
    const t = readFileSync(p, "utf8");
    // 1) 仅 <I ...> 标签内的 name(排除表单 name="email" 等)
    for (const tag of t.matchAll(/<I\b[^>]*>/gs)) {
      const seg = tag[0];
      const lit = seg.match(/\bname="([a-z][a-z0-9-]*)"/);
      if (lit) names.add(lit[1]);
      const expr = seg.match(/\bname=\{([^}]*)\}/);
      if (expr) for (const m of expr[1].matchAll(/"([a-z][a-z0-9-]*)"/g)) names.add(m[1]);
    }
    // 2) icon: "..." 数据对象
    for (const m of t.matchAll(/\bicon:\s*"([a-z][a-z0-9-]*)"/g)) names.add(m[1]);
    // 3) Upload 分享按钮数组 { name: "x", bg|title: ... }
    for (const m of t.matchAll(/\bname:\s*"([a-z][a-z0-9-]*)",\s*(?:bg|title):/g)) names.add(m[1]);
  }
}
walk(srcDir);

// 仅保留 lucide 真实存在的图标(按单图标文件是否存在判定),跳过拼写错误/不存在项
const valid = [];
const skipped = [];
for (const name of [...names].sort()) {
  if (existsSync(join(iconDir, `${name}.mjs`))) valid.push([name, pascal(name)]);
  else skipped.push(name);
}

const lines = [
  "// AUTO-GENERATED by scripts/gen-icon-map.mjs — 请勿手改。",
  "// 新增图标后重跑:  node scripts/gen-icon-map.mjs",
  "// 深度导入单图标文件绕过 lucide-react barrel,让打包器只保留用到的图标。",
  ...valid.map(([name, comp]) => `import ${comp} from "${importPath(name)}";`),
  "",
  "export const ICON_MAP = {",
  ...valid.map(([name, comp]) => `  "${name}": ${comp},`),
  "};",
  "",
];
writeFileSync(outFile, lines.join("\n"));
console.log(`✓ wrote ${outFile}`);
console.log(`  icons: ${valid.length}`);
if (skipped.length) console.log(`  skipped (not in lucide): ${skipped.join(", ")}`);
